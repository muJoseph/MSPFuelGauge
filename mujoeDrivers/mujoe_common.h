// DCO Clock Frequencies
typedef enum
{
    DCO_FREQ_1MHZ = 0x00,
    DCO_FREQ_8MHZ,
    DCO_FREQ_12MHZ,
    DCO_FREQ_16MHZ,

}dcoClkFreq_t;

typedef enum
{
    XTALCAP_DISABLED = XCAP_0,
    XTALCAP_6PF      = XCAP_1,
    XTALCAP_10PF     = XCAP_2,
    XTALCAP_12PF5    = XCAP_3,

}xtalTankCap_t;

typedef struct sysClkConfig_def
{
    dcoClkFreq_t        dcoFreq;
    bool                xtalPop;
    xtalTankCap_t       xtalTankCap;

}sysClkConfig_t;

